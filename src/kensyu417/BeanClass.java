package kensyu417;

// BeanClass: スコアを保持するシンプルなクラス
public class BeanClass {
    private int score; // フィールド: スコアを格納する

    // score フィールドの値を取得する getter (質問1)
    public int getScore() {
        return this.score;
    }

    // score フィールドに値を設定する setter (質問2)
    public void setScore(int score) {
        this.score = score;
    }
}
